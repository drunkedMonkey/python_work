places = [ 'Japan', 'Iceland', 'Italy', 'France','Norway']
print(f"Orden original {places}")

print(sorted(places))

print(f"Orden original {places}")

print(sorted(places, reverse=True))

print(f"Orden original {places}")

places.reverse()
print(f"Orden inverso {places}")

places.reverse()
print(f"Orden original {places}")   

places.sort()
print(f"Ordenado {places}") 

places.sort(reverse=True)
print(f"Ordenado inverso {places}")
